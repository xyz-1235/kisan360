let currentLang = 'en';

function setLanguage(lang, suppressEvent = false) {
    if (!translations[lang]) return;
    currentLang = lang;
    
    // Update active button state
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active', 'bg-emerald-600', 'text-white');
        btn.classList.add('bg-white', 'text-slate-600'); // Reset style
        if (btn.dataset.lang === lang) {
            btn.classList.remove('bg-white', 'text-slate-600');
            btn.classList.add('active', 'bg-emerald-600', 'text-white');
        }
    });

    // Update text content
    document.querySelectorAll('[data-lang-key]').forEach(element => {
        const key = element.getAttribute('data-lang-key');
        if (translations[lang][key]) {
            // Check if element is an input with placeholder
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                element.innerHTML = translations[lang][key];
            }
        }
    });

    // Save preference
    localStorage.setItem('kisan360_lang', lang);
    
    // Dispatch event for other scripts to react
    if (!suppressEvent) {
        window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
    }
}

// Initialize Language on Load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('kisan360_lang') || 'en';
    setLanguage(savedLang);
    
    // Attach event listeners to language buttons if they exist
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => {
             setLanguage(btn.dataset.lang);
        });
    });
});
